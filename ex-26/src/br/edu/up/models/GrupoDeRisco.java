package br.edu.up.models;

public class GrupoDeRisco {
  private int baixo = 0;
  private int medio = 0;
  private int alto = 0;
  
  public int getBaixo() {
    return baixo;
  }
  public void setBaixo(int baixo) {
    this.baixo = baixo;
  }
  public int getMedio() {
    return medio;
  }
  public void setMedio(int medio) {
    this.medio = medio;
  }
  public int getAlto() {
    return alto;
  }
  public void setAlto(int alto) {
    this.alto = alto;
  }
}
